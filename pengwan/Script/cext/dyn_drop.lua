function setex( dyid, confid ) --默认掉落表
	local conf = DynDropTableList[confid]
	if nil==conf then
		look("DynDropTable.setex(失败,dyid: " .. tostring( dyid) .. " confid : " .. tostring(confid) )
	end
	if not set( dyid, conf) then
		look("DynDropTable.set 失败,dyid: " .. tostring( dyid) .. " confid : " .. tostring(confid) )
	end
end
function setex_active( dyid, confid ) --运营活动掉落表
	local conf= DynDropTableList[confid]
	if nil==conf then
		look("DynDropTable.setex(失败,dyid: " .. tostring( dyid) .. " confid : " .. tostring(confid) )
	end

	local elseconf = DynDrop_active[confid]
	if nil==elseconf then
		look("setex_active(失败,dyid: " .. tostring( dyid) .. " confid : " .. tostring(confid),1 )
	end

	for k,v in pairs(elseconf) do
		if type(k)==type(0)  and  type(v)==type({})  then
			table.insert(conf,v)
		end
	end

	if not set( dyid, conf) then
		look("DynDrop_active.set 失败,dyid: " .. tostring( dyid) .. " confid : " .. tostring(confid) ,1)
	end
end
--加载动态掉落表
function Load_DynDrop()
	--look(TRACE_BEGIN(),1)
	--look('加载动态掉落表',1)
	for i,v in pairs(DynDropTableList) do
		if type(DynDrop_active[i])==type({}) then --有运营活动则掉落为活动配置+默认
			setex_active(i,i)
		else
			setex(i,i)
		end
	end
	--look(#DynDropTableList[1],1)
	--look(TRACE_END(),1)
end


--boss运营活动调用--itype=1开始,0结束
function Boss_activecall(itype,msg)
	--look('boss运营活动调用--itype=1开始,0结束',1)
	--look(itype,1)
	--look(msg,1)
	if itype==1 then
		if type(msg)~=type({}) then return end
		DynDrop_active=msg 
	else	
		if type(msg)~=type({}) then return end
		DynDrop_active={}
	end	
	Load_DynDrop()
end
